distance_in_meters = 1000

meters_to_inches = 39.3701
meters_to_feet = 3.28084
meters_to_yards = 1.09361
meters_to_miles = 0.000621371

distance_in_inches = distance_in_meters * meters_to_inches
distance_in_feet = distance_in_meters * meters_to_feet
distance_in_yards = distance_in_meters * meters_to_yards
distance_in_miles = distance_in_meters * meters_to_miles

print("Distance in meters: {:.2f}".format(distance_in_meters))
print("Distance in inches: {:.2f}".format(distance_in_inches))
print("Distance in feet: {:.2f}".format(distance_in_feet))
print("Distance in yards: {:.2f}".format(distance_in_yards))
print("Distance in miles: {:.2f}".format(distance_in_miles))
